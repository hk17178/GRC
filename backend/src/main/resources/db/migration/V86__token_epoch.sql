-- V86 · 会话吊销：app_user.token_epoch（安全评审 M-15）
-- =============================================================
-- JWT 带 ep 声明 = 签发时用户的 token_epoch。登出 / 改密 / 管理员踢下线时 token_epoch += 1，
-- 使该用户此前签发的全部 JWT 因 ep 不匹配立即失效——补齐"无服务端会话失效"缺口。
-- 无 ep 声明的历史令牌（本迁移前签发）跳过校验，随过期或重登自然收敛。
-- =============================================================

ALTER TABLE app_user ADD COLUMN token_epoch INT NOT NULL DEFAULT 0;
